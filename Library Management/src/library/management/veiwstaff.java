    private void fetchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fetchActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
        try{
            Class .forName("oracle.jdbc.driver.OracleDriver");
            Connection con=DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:xe ","system","system");
            PreparedStatement ps=con.prepareStatement("SELECT * FROM TEACHER");
            ResultSet res=ps.executeQuery();
            while(res.next()){
                String id=res.getString("ID");
                String name=res.getString("NAME");
                int sa=res.getInt("SALARY");
                String authour=res.getString("CONTACT_NO");
                String dep=res.getString("DEPARTMENT");
                String des=res.getString("DESIGNATION");
                model.addRow(new Object[]{id,name,sa,authour,dep,des});

            }

        }
        catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_fetchActionPerformed

