    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String id=V1.getText();
        String name=V2.getText();
        int salary=Integer.parseInt(V3.getText());
        String No=V4.getText();
        String  Dept=V5.getText();
        String des=V6.getText();
        try{
        Class.forName("oracle.jdbc.driver.OracleDriver");
        Connection con=DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:xe ","system","system");
        PreparedStatement ps=con.prepareStatement("insert into TEACHER values(?,?,?,?,?,?)");
        ps.setString(1,id);
        ps.setString(2,name);
        ps.setString(4, No);
        ps.setInt(3,salary);
        ps.setString(5,Dept);
        ps.setString(6,des);
        int res=ps.executeUpdate();
        if(res>0){
            JOptionPane.showMessageDialog(this,"BOOK ADDED SUCESSFULLY");
            V1.setText(null);
            V2.setText(null);
            V3.setText(null);
            V4.setText(null);
            V5.setText(null);
            V6.setText(null);
        }
        }
        catch(Exception e){
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

